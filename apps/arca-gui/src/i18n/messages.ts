export const messages: Record<Locale, Record<MessageKey, string>> = { en, ko };

export function resolveLocale(value: string | null | undefined): Locale {
  return value?.toLowerCase().startsWith("ko") ? "ko" : "en";
}

export function readLocalePreference(): Locale | null {
  try {
    const rawLocale = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    return rawLocale === "ko" || rawLocale === "en" ? rawLocale : null;
  } catch {
    return null;
  }
}

export function writeLocalePreference(locale: Locale) {
  try {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  } catch {
    // Persisting locale is a convenience; the UI can still switch for this session.
  }
}

export function detectLocale(): Locale {
  return resolveLocale(typeof navigator === "undefined" ? undefined : navigator.language);
}

export function currentLocale(): Locale {
  return readLocalePreference() ?? detectLocale();
}

export function formatMessage(template: string, params?: MessageParams): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{([^}]+)\}/g, (match, key: string) => {
    const value = params[key];
    return value === undefined ? match : String(value);
  });
}

export function translate(locale: Locale, key: MessageKey, params?: MessageParams): string {
  return formatMessage(messages[locale][key] ?? messages.en[key], params);
}
